/**
 * Role-to-resources mapping for quick lookup.
 * Maps normalized role names to resource IDs.
 */
export function getResourcesForRole(targetRole) {
  if (!targetRole) return resources;

  const roleLower = targetRole.toLowerCase();

  // Fuzzy match the target role to our known role tags
  const roleMatchers = [
    { keywords: ["data analyst", "data analysis", "analyst", "bi analyst"], role: "Data Analyst" },
    { keywords: ["data engineer", "data engineering", "etl developer", "pipeline engineer"], role: "Data Analyst" },
    { keywords: ["data scientist", "data science"], role: "Machine Learning Engineer" },
    { keywords: ["business analyst", "business intelligence", "ba "], role: "Data Analyst" },
    { keywords: ["frontend", "front-end", "front end", "ui engineer", "ui developer", "web developer", "react developer"], role: "Frontend Engineer" },
    { keywords: ["ui/ux", "ux designer", "ui designer", "product designer", "interaction design"], role: "Frontend Engineer" },
    { keywords: ["backend", "back-end", "back end", "server side", "api developer"], role: "Frontend Engineer" },
    { keywords: ["full stack", "fullstack", "full-stack", "mern", "mean stack"], role: "Frontend Engineer" },
    { keywords: ["machine learning", "ml engineer", "deep learning"], role: "Machine Learning Engineer" },
    { keywords: ["ai engineer", "artificial intelligence", "llm", "genai", "generative ai"], role: "Machine Learning Engineer" },
    { keywords: ["devops", "dev ops", "site reliability", "sre", "infrastructure"], role: "Machine Learning Engineer" },
    { keywords: ["cloud engineer", "cloud architect", "aws", "azure", "gcp"], role: "Machine Learning Engineer" },
    { keywords: ["mobile", "ios developer", "android developer", "react native", "flutter"], role: "Frontend Engineer" },
    { keywords: ["cybersecurity", "security analyst", "infosec", "information security", "penetration"], role: "Machine Learning Engineer" },
    { keywords: ["product manager", "product owner", "pm "], role: "Data Analyst" },
    { keywords: ["project manager", "scrum master", "agile coach"], role: "Data Analyst" },
    { keywords: ["qa engineer", "quality assurance", "test engineer", "sdet", "automation tester"], role: "Machine Learning Engineer" },
  ];

  const matchedRole = roleMatchers.find((m) => m.keywords.some((k) => roleLower.includes(k)));

  if (matchedRole) {
    return resources.filter((r) => r.roles.includes(matchedRole.role));
  }

  // No exact match — return all resources
  return resources;
}
